package reCapDemo_Classes;

public class FourOperations {
	
		public int Add(int num1, int num2) {
			return num1 + num2;
		}
		public int Substraction(int num1, int num2) {
			return num1 - num2;
		}
		public int Multiply(int num1, int num2) {
			return num1 * num2;
		}
		public int Divide(int num1, int num2) {
			return num1 / num2;
		}
	
}
