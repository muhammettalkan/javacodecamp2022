package javaCCWeek3;

public class Customer {
	public Customer() {
		System.out.println("Customer object has started.");
	}
	public int id;
	public String city;
	
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	
	
	
}
